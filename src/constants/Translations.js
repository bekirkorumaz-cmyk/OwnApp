const deepMerge = (base, override = {}) => Object.keys(override).reduce((next, key) => {
  const value = override[key];
  next[key] = value && typeof value === 'object' && !Array.isArray(value)
    ? deepMerge(base[key] || {}, value)
    : value;
  return next;
}, { ...base });

export const TRANSLATIONS = SUPPORTED_LANGUAGES.reduce((packs, { code }) => {
  if (code === 'tr-TR') {
    packs[code] = tr;
  } else {
    packs[code] = deepMerge(en, languageOverrides[code]);
  }
  return packs;
}, {});

export const getTranslationValue = (language, key) => {
  const keys = key.split('.');
  const read = (pack) => keys.reduce((value, part) => value?.[part], pack);
  return read(TRANSLATIONS[language]) ?? read(TRANSLATIONS[DEFAULT_LANGUAGE]) ?? key;
};

export const formatTranslation = (template, params = {}) => (
  typeof template === 'string'
    ? template.replace(/\{(\w+)\}/g, (_match, key) => (
    params[key] == null ? '' : String(params[key])
  ))
    : template
);
